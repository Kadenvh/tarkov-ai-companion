// Bundle the @tac/service and @tac/agent sidecars into standalone ESM files
// that a packaged Electron build runs on the bundled Node 26 runtime — no
// pnpm, no tsx, no workspace node_modules required on the target machine.
//
// Output:
//   dist-electron/sidecars/service.mjs   (fully self-contained)
//   dist-electron/sidecars/agent.mjs     (self-contained except the Claude
//                                          Agent SDK — see AGENT_EXTERNALS)
//
// Strategy: esbuild, platform:node, target:node26, format:esm, bundle:true.
// We bundle every workspace (@tac/*) + npm dependency INTO the output. Node
// builtins (including `node:sqlite`, which the state-engine uses and which the
// bundled Node 26 provides natively) stay external — esbuild treats every
// `node:`-prefixed and known-builtin import as external under platform:node.
//
// AGENT_EXTERNALS: `@anthropic-ai/claude-agent-sdk` is NOT bundled. It ships as
// a pre-bundled ESM blob that unpacks an embedded ~242 MB Claude Code binary at
// runtime via `import.meta.url` + a bunfs extractor; inlining it would both
// break that path resolution and bloat the installer. The agent imports it
// lazily (`await import(...)`) with a graceful `BackendUnavailableError`
// fallback, so the agent server still boots when the package is absent — the
// mock backend always works and the `api` backend (plain @anthropic-ai/sdk,
// which IS bundled) works with an ANTHROPIC_API_KEY. The `agent-sdk` backend
// rides THIS machine's Claude Code login and therefore expects the SDK to be
// resolvable from a node_modules beside agent.mjs (or a global install); see
// the report / README for how to provision it if that backend is wanted in the
// installed app.
//
// @tier T0 (build tooling — no game-process contact).

import { build } from "esbuild";
import { rm, mkdir } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const repoRoot = resolve(root, "..", "..");
const outdir = resolve(root, "dist-electron", "sidecars");

await mkdir(outdir, { recursive: true });

// ESM output that bundles CJS npm deps (fastify et al.) needs a working
// `require` / `__dirname` / `__filename` for any dep that reaches for them at
// runtime. Provide them from the module's own URL.
const esmShimBanner = {
  js: [
    "import { createRequire as __tacCreateRequire } from 'node:module';",
    "import { fileURLToPath as __tacFileURLToPath } from 'node:url';",
    "import { dirname as __tacDirname } from 'node:path';",
    "const require = __tacCreateRequire(import.meta.url);",
    "const __filename = __tacFileURLToPath(import.meta.url);",
    "const __dirname = __tacDirname(__filename);",
  ].join("\n"),
};

/** Externals per sidecar — see AGENT_EXTERNALS above. */
const AGENT_EXTERNALS = ["@anthropic-ai/claude-agent-sdk"];

const shared = {
  bundle: true,
  platform: "node",
  target: "node26",
  format: "esm",
  sourcemap: true,
  logLevel: "info",
  // Prefer the ESM/source entry of workspace deps.
  mainFields: ["module", "main"],
  conditions: ["node", "import"],
  banner: esmShimBanner,
};

/** @param {{name:string, entry:string, outfile:string, external?:string[], mainFields?:string[], conditions?:string[]}} spec */
async function bundleSidecar(spec) {
  console.log(`[build-sidecars] bundling ${spec.name} <- ${spec.entry}`);
  await build({
    ...shared,
    absWorkingDir: repoRoot,
    entryPoints: [spec.entry],
    outfile: spec.outfile,
    external: spec.external ?? [],
    // Per-sidecar resolution override. The service must resolve CJS deps
    // (fastify + `ws`) via their `require`/`main` entry, NOT the ESM wrapper:
    // `ws`'s ESM wrapper drops the static `.Server`/`.WebSocketServer` props that
    // @fastify/websocket calls as `new WebSocket.Server(...)`, so an ESM-first
    // resolve bundles a `ws` whose `.Server` is undefined at runtime.
    ...(spec.mainFields ? { mainFields: spec.mainFields } : {}),
    ...(spec.conditions ? { conditions: spec.conditions } : {}),
    // Keep the generated file identifiable.
    banner: {
      js: `// @tac/${spec.name} sidecar bundle (generated by build-sidecars.mjs) — do not edit\n${esmShimBanner.js}`,
    },
  });
  console.log(`[build-sidecars] wrote ${spec.outfile}`);
}

await bundleSidecar({
  name: "service",
  entry: resolve(repoRoot, "apps", "service", "src", "main.ts"),
  outfile: resolve(outdir, "service.mjs"),
  // CJS-first so fastify + `ws` resolve to their require entry (keeps ws.Server).
  mainFields: ["main", "module"],
  conditions: ["node", "require"],
});

await bundleSidecar({
  name: "agent",
  entry: resolve(repoRoot, "apps", "agent", "src", "main.ts"),
  outfile: resolve(outdir, "agent.mjs"),
  external: AGENT_EXTERNALS,
});

console.log("[build-sidecars] done — dist-electron/sidecars/{service,agent}.mjs");
