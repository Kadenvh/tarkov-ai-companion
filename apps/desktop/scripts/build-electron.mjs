// Build the Electron main + preload with esbuild.
//
// The package is ESM ("type":"module"), so the main process is emitted as ESM
// (`main.js`) — Electron 43 runs ESM main. The preload MUST be CommonJS because
// sandboxed preload scripts cannot be ES modules, so it is emitted as
// `preload.cjs`. `electron` and Node builtins stay external (provided by the
// Electron runtime); our own `src/lib/*` helpers are bundled in.
//
// @tier T0

import { build } from "esbuild";
import { rm, mkdir } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const outdir = resolve(root, "dist-electron");

await rm(outdir, { recursive: true, force: true });
await mkdir(outdir, { recursive: true });

const shared = {
  bundle: true,
  platform: "node",
  target: "node26",
  external: ["electron"],
  sourcemap: true,
  logLevel: "info",
};

await build({
  ...shared,
  entryPoints: [resolve(root, "src/main.ts")],
  outfile: resolve(outdir, "main.js"),
  format: "esm",
  // Node builtins are imported with the `node:` prefix; keep the banner clean.
  banner: { js: "// @tac/desktop main (generated by esbuild) — do not edit" },
});

await build({
  ...shared,
  entryPoints: [resolve(root, "src/preload.ts")],
  outfile: resolve(outdir, "preload.cjs"),
  format: "cjs",
  banner: { js: "// @tac/desktop preload (generated by esbuild) — do not edit" },
});

console.log("[build-electron] wrote dist-electron/main.js + preload.cjs");
